export default function Home() {
  // State and methods for the editor
  const editorRef = useRef(null);
  const [isEditorReady, setIsEditorReady] = useState(false);
  const [__log, setLog] = useState("Console");
  const handleEditorDidMount = (editor, monaco) => {
    editorRef.current = editor;
    setIsEditorReady(true);
  };
  const getValue = () => editorRef.current.getValue();
  const handleEditorChange = async (value, event) => {
    let sandbox = document.getElementById("sandbox");

    // clearLog();
    let response = await fetch("/api/runcode", {
      method: "POST",
      body: JSON.stringify({ code: value }),
    });
    setLog(await response.json());
  };

  var defaultJSValue = `// Write your JavaScript code below

  console.log("Hello, world. EDIT ME AND SEE...")
  
  for (let i=0; i<3; i++) {
      console.log("log")
  }
  
  undefinedVariable`;

  return (
    <>
      <Head>
        <title>Daemon</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main>
        {/* <h1 className="text-2xl font-bold">Daemon</h1> */}
        <div id="ide">
          <Editor
            defaultLanguage="javascript"
            defaultValue={defaultJSValue}
            onChange={handleEditorChange}
            onMount={handleEditorDidMount}
            options={{
              lineNumbers: "on",
              fontFamily: "Source Code Pro",
              fontSize: "15px",
            }}
          />
        </div>
        <div id="console">
          <pre>{__log}</pre>
        </div>
        <div id="sandboxContainer">
          <iframe id="sandbox"></iframe>
        </div>
      </main>
    </>
  );
}
